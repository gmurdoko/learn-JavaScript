function SwitchCase(input) {
    switch (input) {
        case "kucing":
            console.log("Meowww~");
            break;
        case "anjing":
            console.log("Guk Guk");
            break;
        case "sapi":
            console.log("Moooooo");
            break;
        default:
            console.log("hmm hmm");
            break;
    }
}

SwitchCase("kucing");
SwitchCase("anjing");
SwitchCase("sapi");
