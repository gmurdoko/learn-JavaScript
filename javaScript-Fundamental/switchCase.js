let hari = 10;
switch (hari) {
    case 1:
        console.log("Minggu");
        break;
    case 2:
        console.log("Senin");
        break;
    case 3:
        console.log("Selasa");
        break;
    case 4:
        console.log("Rabu");
        break;
    case 5:
        console.log("Kamis");
        break;
    case 6:
        console.log("Jum'at");
        break;
    case 7:
        console.log("Sabtu");
        break;
    default:
        console.log("Invalid Day");
        break;
}

